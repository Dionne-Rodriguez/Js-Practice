// When provided with a number between 0-9, return it in words.
//
// Input :: 1
//
// Output :: "One".

function switchItUp(number){
switch (number) {
  case 0:
    return "zero";
    break;
  case 1:
     return "One";
    break;
  case 2:
      return "Two";
    break;
  case 3:
    return "Three";
    break;
  case 4:
     return "four";
    break;
  case 5:
     return "Five";
    break;
  case 6:
   return "Six";
    case 7:
     return "Seven";
    break;
     case 8:
     return "Eight";
    break;
     case 9:
     return "Nine";
    break;
}
}
